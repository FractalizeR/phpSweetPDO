\phpSweetPDO\Autoloader::register();

/**
 * Test class for Basic.
 * Generated by PHPUnit on 2011-05-01 at 03:31:36.
 */
class BasicTest extends PHPUnit_Framework_TestCase
{
    public function testInsert()
    {
        $sql = \phpSweetPDO\SQLHelpers\Basic::insert('test', array('id' => 'test'));
        $this->assertEquals("INSERT INTO  `test` (id) VALUES (:id)", $sql[0]);
        $this->assertEquals(array('id' => 'test'), $sql[1]);
    }

    public function testUpdateSimple()
    {
        $sql = \phpSweetPDO\SQLHelpers\Basic::update('test', array('id' => 'test'));
        $this->assertEquals("UPDATE `test` SET id=:id", $sql[0]);
        $this->assertEquals(array('id' => 'test'), $sql[1]);
    }

    public function testUpdateWithCriteria()
    {
        $sql = \phpSweetPDO\SQLHelpers\Basic::update('test', array('id' => 'test'), "alpha='beta'");
        $this->assertEquals("UPDATE `test` SET id=:id WHERE alpha='beta'", $sql[0]);
        $this->assertEquals(array('id' => 'test'), $sql[1]);
    }
}


